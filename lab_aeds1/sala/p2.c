double kilo_para_grama(double k){
    return k * 1000;
}
double kilo_para_arroba(double k){
    return k / 15;
}
double arroba_para_grama(double a){
    return a * 15000;
}
double arroba_para_kilo(double a){
    return a * 15 ;
}
double grama_para_kilo(double g){
    return g / 1000;
}
double grama_para_arroba(double g){
    return g / 15000;
}